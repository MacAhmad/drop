    function whatsapp(){
        
        var nom = document.getElementById("cli_name").value;
        var mail = document.getElementById("cli_email").value;
        var adresse = document.getElementById("cli_address").value;
        var ville = document.getElementById("cli_city").value;
        var tel = document.getElementById("cli_phone").value;
        var subject = document.getElementById("cli_subj").value;
        var quantite = document.getElementById("cli_qty").value;
        var duedate = document.getElementById("cli_date").value;
        

        var whatsappurl = "https://wa.me/655030050?text="
        + "Nom du client:" + nom + "%0a"
        + "Email du client:" + mail + "%0a"
        + "Adresse du client:" + adresse + "%0a"
        + "Ville:" + ville + "%0a"
        + "Contact du client:" + tel + "%0a"
        + "Objet:" + subject + "%0a"
        + "Quantite:" + quantite + "%0a"
        + "Delai de livraison:" + duedate;

        window.open(whatsappurl).focus();
    }

